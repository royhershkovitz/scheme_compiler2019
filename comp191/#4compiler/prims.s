%macro violation_check 2
    mov rdx, %2
    movzx rbx, byte [%1]
    cmp dl, bl
    jne contract_violation
%endmacro

%macro pair_proluge 0 
    push rbp
    mov rbp, rsp

    mov rsi, PVAR(0)
    violation_check rsi, T_PAIR;verify that is pair
%endmacro

