SOB_EMPTY_ENV:
    db T_VECTOR
    dq 0

out_of_ram:
    mov rdi, contract_violation.out_of_ram
    jmp contract_violation.kill

apply_bad_f:
	mov rdi, contract_violation.apply_bad_f
    jmp contract_violation.kill

apply_fail:
	mov rdi, contract_violation.apply_err
    jmp contract_violation.kill

bad_arguments_count:
	mov rdi, contract_violation.bad_arg_coung_msg
    jmp contract_violation.kill

contract_violation:
    ;rdx expected
    ;rsi actual
    cmp bl, 12
    jge .ok 
	mov rsi, qword [.jump_tbl_types + rbx * 8]
	mov rdx, qword [.jump_tbl_types + rdx * 8]
	mov rdi, .exit_format
    jmp .kill

.ok:
    mov rdi, .fail_type

.kill:
    mov rax, 0
	call printf
    mov rax, 60       
    mov rdi, -2     ;EXIT_Failure types
    syscall

