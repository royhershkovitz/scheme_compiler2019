push_proper:
    cmp rbx, SOB_NIL_ADDRESS
    je apply.cont_apply
    inc rcx
    push qword [rbx + TYPE_SIZE]
    mov rbx, qword [rbx + TYPE_SIZE + WORD_SIZE];appending as ending list
    jmp push_proper

;rbp will be pushed later the entering execution will push him do that
;get a list, push its organs and the number of organs
apply:
    pop r10;ret
    add rsp, 2*8;pop env and argno
    pop rax;f
    pop rbx;magic arglist
    mov rcx, 0;count the list length

    movzx r11, byte [rax];check type
    cmp r11, T_CLOSURE
    jne apply_bad_f

    jmp push_proper
.cont_apply:
    push rcx;argno
    push r10;env will be overriden in tp
    push r10;ret
    REST_APPLICTP;jmp to f

